# Suma 
a = int(input("Escriba el valor de a: "))
b = int(input("Escriba el valor de b: "))
#Para convertir string a entero también puede ser 'respuesta = int(a) + int (b)'

respuesta = a + b
print("La suma de los dos valores es: ", respuesta)

#Resta
respuesta = a - b
print("La resta de los dos valores es: ", respuesta)

#División
respuesta = a / b
print("La división de los dos valores es: ", respuesta)

#Multiplicación
respuesta = a * b
print("La multiplicación de los dos valores es: ", respuesta)