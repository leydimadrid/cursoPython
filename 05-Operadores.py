"""Los operadores son símbolos que le indican al 
intérprete que realice una operación específica,
 como aritmética, comparación, lógica"""

#Operador aritméticos 
# Suma = +
# Resta = -
# Multiplicación = *
# División = /
# Potencia = ** La potencia es multiplicar un número las veces que necesitamos. 
# Ejm: a = 7 c = a ** 3
# Módulo = % Residuos de la división
# Cociente = //

a = 14
b = 3
c = a / b

print(c)

#Operadores de comparación
"""Nos permiten saber la relación existente entre dos variables."""
# Igual que: ==
# Diferente que: !=
# Mayor que > (Alt + 62)
# Menor que < (Alt + 60)

x = "Juan"
y = "Carlos"

print(x == y)

#Operadores de asignación
# Igual: =
# Suma su mismo valor: +=
# Resta su mismo valor: -=
# Multiplica su mismo valor: *=

x = 9
x += x

print(x)